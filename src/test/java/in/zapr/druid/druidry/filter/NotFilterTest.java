package in.zapr.druid.druidry.filter;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import org.json.JSONException;
import org.json.JSONObject;
import org.skyscreamer.jsonassert.JSONAssert;
import org.skyscreamer.jsonassert.JSONCompareMode;
import org.testng.annotations.BeforeClass;
import org.testng.annotations.Test;

public class NotFilterTest {

    private static ObjectMapper objectMapper;

    @BeforeClass
    public void init() {
        objectMapper = new ObjectMapper();
    }

    @Test
    public void testAllFields() throws JSONException, JsonProcessingException {

        SelectorFilter selectorFilter = new SelectorFilter("Hello", "World");

        NotFilter filter = new NotFilter(selectorFilter);

        JSONObject selectorJson = new JSONObject();
        selectorJson.put("type", "selector");
        selectorJson.put("dimension", "Hello");
        selectorJson.put("value", "World");

        JSONObject jsonObject = new JSONObject();
        jsonObject.put("type", "not");
        jsonObject.put("field", selectorJson);

        String actualJSON = objectMapper.writeValueAsString(filter);
        String expectedJSON = jsonObject.toString();
        JSONAssert.assertEquals(expectedJSON, actualJSON, JSONCompareMode.NON_EXTENSIBLE);
    }

    @Test(expectedExceptions = NullPointerException.class)
    public void testFieldsMissing() {
        NotFilter andFilter = new NotFilter(null);
    }
}